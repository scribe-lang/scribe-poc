///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////// StmtBlock ////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////

bool StmtBlock::const_fold(TypeMgr &types, ValueMgr &vals)
{
	types.pushlayer();
	for(auto &s : stmts) {
		if(!s->const_fold(types, vals)) {
			err::set(line, col, "failed to execute comptime block");
			return false;
		}
	}
	types.poplayer();
	return true;
}

